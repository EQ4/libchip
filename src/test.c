#include <stdio.h>
#include <stdlib.h>
#include "audio.h"

static uint16_t wave_tri[] = {
	0x0, 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7,
	0x8, 0x9, 0xA, 0xB, 0xC, 0xD, 0xE, 0xF,
	0xF, 0xE, 0xD, 0xC, 0xB, 0xA, 0x9, 0x8,
	0x7, 0x6, 0x5, 0x4, 0x3, 0x2, 0x1, 0x0,
	0x0, 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7,
	0x8, 0x9, 0xA, 0xB, 0xC, 0xD, 0xE, 0xF,
	0xF, 0xE, 0xD, 0xC, 0xB, 0xA, 0x9, 0x8,
	0x7, 0x6, 0x5, 0x4, 0x3, 0x2, 0x1, 0x0
};

static uint16_t wave_50[] = {
	0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
	0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
	0xF, 0xF, 0xF, 0xF, 0xF, 0xF, 0xF, 0xF,
	0xF, 0xF, 0xF, 0xF, 0xF, 0xF, 0xF, 0xF
};

int main(int argc, char **argv)
{
	audio_init(44100,4,1024,2,512);
	printf("[main] Waiting for stop signal.\n");

	audio_set_wave(0, &wave_tri[0], 64, 1);

	audio_set_freq(0,2);

	audio_set_amp(0,0xF,0xF);
	char c = 0;
	int started = 0;
	int f = 1;
	while(c != 'z')
	{
		c = getchar();
		if (!started)
		{
			audio_start();
			started = 1;
		}
		audio_set_freq(0,f);
		f++;
	}
	audio_shutdown();
	return 0;
}
